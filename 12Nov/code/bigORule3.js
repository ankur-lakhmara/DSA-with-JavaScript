function printBoxes(boxes){
    for(const item of boxes){
        console.log(item);  
    }
    return 'done';
}


let boxes = [1,2,3,5];

console.log(printBoxes(boxes));

let boxes2 = [1,5,8,9,12,45,]





function printBoxes2(boxes,boxes2){
    for(const item of boxes){
        console.log(item);
    }

    for(const item of boxes2){
        console.log(item);
        
    }
    return 'done2'
}

console.log(printBoxes2(boxes,boxes2));


